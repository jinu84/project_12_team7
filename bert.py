from sentence_transformers import SentenceTransformer, util

# 응답 텍스트
rag = """갤럭시 S 울트라에서 인물 사진을 새롭게 만드는 방법은 다음과 같습니다:
갤러리 앱을 실행하고 원하는 사진 파일을 선택하세요.
어시스트 아이콘을 누르고 인물 사진 스튜디오를 선택하세요.
원하는 스타일을 선택한 후 생성을 누르세요.
원하는 결과물을 선택한 후 다른 파일로 저장을 눌러 이미지를 저장하세요."""

gpt4o = """갤럭시 S 울트라에서 인물 사진을 새롭게 만드는 방법은 여러 가지가 있습니다. 여기 몇 가지 팁을 드리겠습니다:

포트레이트 모드 사용: 갤럭시 S 울트라에는 인물 사진을 찍을 때 배경을 흐리게 만들어 피사체를 돋보이게 하는 포트레이트 모드가 있습니다. 이 모드를 사용하면 더욱 전문적인 느낌의 사진을 얻을 수 있습니다.

조명 활용: 자연광을 잘 활용하거나, 필요에 따라 인공 조명을 사용하여 피사체를 잘 조명해 보세요. 좋은 조명은 사진의 분위기를 크게 좌우합니다.

구도와 각도: 다양한 각도에서 사진을 찍어보고, 피사체를 중앙에 두기보다는 약간 옆으로 배치해보세요. 삼분할 구도를 활용하면 더욱 균형 잡힌 사진을 얻을 수 있습니다.

후보정: 갤럭시 S 울트라에는 기본적으로 제공되는 편집 기능이 있습니다. 사진을 찍은 후 밝기, 대비, 색상 등을 조정하여 원하는 느낌을 더할 수 있습니다.

렌즈 선택: 갤럭시 S 울트라는 여러 개의 렌즈를 제공하므로, 상황에 맞게 적절한 렌즈를 선택하여 다양한 효과를 시도해 보세요.

이러한 방법들을 활용하면 갤럭시 S 울트라로 더욱 창의적이고 멋진 인물 사진을 찍을 수 있을 것입니다."""

gpt35 = """갤럭시 S울트라에서 인물 사진을 새롭게 만드는 방법은 다양한 사진 편집 앱을 활용하는 것이 일반적입니다. 갤럭시 스토어나 구글 플레이 스토어에서 다양한 사진 편집 앱을 다운로드하여 사용할 수 있습니다. 이 앱들을 사용하여 필터를 적용하거나 조명을 조절하여 사진을 더 아름답게 만들 수 있습니다. 또한, 얼굴 인식 및 보정 기능을 제공하는 앱을 사용하여 얼굴의 피부 톤을 조절하거나 주근깨를 제거하는 등의 작업을 할 수도 있습니다. 이를 통해 보다 멋진 인물 사진을 만들 수 있을 것입니다."""

# 모델 불러오기 (KoSBERT)
model = SentenceTransformer('jhgan/ko-sbert-sts')

# 임베딩 생성
embeddings = model.encode([rag, gpt4o, gpt35], convert_to_tensor=True)

# 유사도 계산
cos_sim = util.cos_sim(embeddings, embeddings)

# 결과 출력
print("RAG ↔ GPT-4o:", cos_sim[0][1].item())
print("RAG ↔ GPT-3.5:", cos_sim[0][2].item())
print("GPT-4o ↔ GPT-3.5:", cos_sim[1][2].item())
